#Main()
def main():
        print("Enter Text:\n")

        #User Input
        s1,s2,s3=map(str,raw_input().split())

        #Result
        print("\ns1={0}\ns2={1}\ns3={2}\n".format(s1,s2,s3))


#Setting top level conditional script
if __name__=='__main__':
        main()




