#Using Pointers in Expressions

#Main()
def main():

        #Variable Declaration
        i1=5
        p1=i1
        i2=p1/2+10
        p2=p1

        #Result
        print("i1 = {0}, i2 = {1}, p1 = {2}, p2 = {3}\n".format(i1, i2, p1, p2))

#Setting top level conditional script
if __name__=='__main__':
        main()



