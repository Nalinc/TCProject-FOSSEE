#More Pointer Basics

#Main()
def main():

        #Variable Declaration
        c='Q'
        char_pointer=c
        print("{0} {1}".format(c,char_pointer))

        c='/'
        char_pointer=c
        print("{0} {1}".format(c,char_pointer))

        char_pointer='('
        c=char_pointer
        print("{0} {1}".format(c,char_pointer))



#Setting top level conditional script
if __name__=='__main__':
        main()


